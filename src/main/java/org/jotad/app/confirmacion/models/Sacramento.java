package org.jotad.app.confirmacion.models;

public class Sacramento {
    private Integer id;
    private String nombre;

    public Sacramento() {
    }

    public Sacramento(Integer id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
